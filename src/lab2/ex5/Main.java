package lab2.ex5;
import java.util.Scanner;

public class Main {
    public static void main(String[] args) {

        // valori intregi
        Scanner in = new Scanner(System.in);
        int x = in.nextInt();
        System.out.println("x = " + x);

        System.out.println();

        // valori in virgula flotanta
        double y = in.nextDouble();
        System.out.println("y = " + y);

    }

}
